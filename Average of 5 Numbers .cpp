#include <iostream>
#include <math.h>

using namespace std;

// C++ Program To find Average of 5 Numbers (You can modify , Its an Example)
int main () {
    float a , b , c, d , e , f ;
    cout << "Enter the Five Numbers In Sequence " << endl;
    cin >> a;
    cin >> b;
    cin >> c;
    cin >> d;
    cin >> e ;

    f = (a + b + c + d + e)/5 ;

    cout << "The Average of Five Numbers is " << f ;

    return 0;

}
